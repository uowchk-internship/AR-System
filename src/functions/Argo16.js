const axios = require('axios');

//Argo16
export const saveArgo16 = async (url, data) => {
    let result = {}
    url+="/api/argo16/"
    await axios.post(url, data)
        .then((response) => {
            if (response.status === 200) {
                result = response.data
            } else {
                result = "No response from server"
            }
        })
        .catch((err) => {
            result = "No response from server"
        })

    return result
}

